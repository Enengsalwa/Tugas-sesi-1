reamur = float(input("masukan dalam reamur"))
celcius = 0.0
fahrenheit = 0.0
kelvin = 0.0

celcius = 5/4 * reamur 
fahrenheit = 9/4 * reamur + 32
kelvin = 5/4 * reamur + 273

print("reamur %2.f -> celcius :%.2f"%(reamur,celcius))
print("reamur %2.f -> fahrenheit :%.2f"%(reamur,fahrenheit))
print("reamur %2.f -> kelvin :%.2f"%(reamur,kelvin))